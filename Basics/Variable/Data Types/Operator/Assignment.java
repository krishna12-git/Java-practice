public class Assignment{
    public static void main(String[] args) {
        int x = 10;

        x += 5;  // x = x + 5
        System.out.println(x); // 15

        x -= 3;
        System.out.println(x); // 12

        x *= 2;
        System.out.println(x); // 24

        x /= 4;
        System.out.println(x); // 6
    }
}